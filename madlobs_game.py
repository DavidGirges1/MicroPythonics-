# madlibs game project
# a word game to create a story by filling in random words 

day = input("Enter a day: ")
place = input("enter a place: ")
someone = input("enter anyone come to your mind: ")
noun = input("enter any noun: ")
adjective = input("enter a describing adjective ends with ing: ")
adjective2 = input("enter a personal adjective: ")

story = (f""" 
Today is {day}. I went to the {place}. I went with my {someone}.
There I saw a {noun}. and it was {adjective}. I was {adjective2}

""")

print(story)







